"""Operaciones aritmética con python"""

"""Operador potencia: **"""

potencia1 = 10**3

print("El resultado de mi potencia1 es: {}".format(potencia1))

"""Usando la palabra reserveda 'pow' podemos también elevar a una cierta potencia un número"""
"""Nos pedirá 2 valores, el primero es la base y el segundo es la potencia a ser elevada"""

potencia2 = pow(20, 4)
potencia3 = pow(5, 6)

print("El valor de mi potencia2 es: {}".format(potencia2))
print("El valor de mi potencia3 es: {}".format(potencia3))
