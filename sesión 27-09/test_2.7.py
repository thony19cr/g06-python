"""Listas (reverse): Revierte el orden de los elementos de una lista"""

datosPersona = ["Susana", "Gutierrez", 36, "20/04/1984"]
print("Los datos de mi lista son: {}".format(datosPersona))

datosPersona.reverse()
print("Los valores de mi nueva lista son: {}".format(datosPersona))


