"""Diccionarios en Python"""

"""Los nombres de los key van a ir escritos en mínusculas"""

diccionario = {"nombre": "Python", "tipo": "Backend"}

print("Nuestro primer diccionario tiene este contenido: {}".format(diccionario))

"""Agregar elementos en un nuevo key a mi diccionario"""

diccionario["antiguedad"] = "14 años"
diccionario["sistemasOperativo"] = "Mac, Linux y Windows"

print("Mi diccionario actualizado es: {}".format(diccionario))
